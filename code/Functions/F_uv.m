function Y = F_uv(X)
u = X(:,5) .* cos(X(:,1)) + X(:,6) .* cos(X(:,1) + X(:,2)) + ...
    X(:,7) .* cos(X(:,1) + X(:,2) + X(:,3)) + ...
    X(:,8) .* cos(X(:,1) + X(:,2) + X(:,3)+X(:,4));

v = X(:,5) .* sin(X(:,1)) + X(:,6) .* sin(X(:,1) + X(:,2)) + ...
    X(:,7) .* sin(X(:,1) + X(:,2) + X(:,3)) + ...
    X(:,8) .* sin(X(:,1) + X(:,2) + X(:,3)+X(:,4));

Y = sqrt(u.^2 + v.^2);
end

